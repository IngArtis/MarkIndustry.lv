// Retrieve the currently saved language or default to English
let currentLanguage = localStorage.getItem('language') || 'en';
// Event listener to set language on page load
document.addEventListener('DOMContentLoaded', function() {
    const currentLanguage = localStorage.getItem('language') || 'en';
    changeLanguage(currentLanguage);

    // Set up event listeners for language switch buttons
    document.querySelectorAll('.lang-switch').forEach(button => {
        button.addEventListener('click', function() {
            changeLanguage(this.getAttribute('data-lang'));
        });
    });
});
function changeLanguage(lang) {
    localStorage.setItem('language', lang);
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key');
        if (languages[lang][key]) { // Check if translation exists
            element.textContent = languages[lang][key];
        }
    });
}

// Initialize the image slider functionality
document.addEventListener('DOMContentLoaded', function() {
    const slides = document.querySelectorAll('.slides img');
    let currentSlide = 0;
    slides[currentSlide].style.opacity = 1; // Make the first image fully visible initially.

    setInterval(() => {
        slides[currentSlide].style.opacity = 0; // Hide the current image
        currentSlide = (currentSlide + 1) % slides.length; // Increment or loop over slide index
        slides[currentSlide].style.opacity = 1; // Show the new image
    }, 3000); // Change image every 3000 milliseconds (3 seconds)
});

document.addEventListener('DOMContentLoaded', function() {
    const allSliders = document.querySelectorAll('.image-container');

    allSliders.forEach(slider => {
        const slides = slider.querySelectorAll('.slide');
        let currentSlide = 0;

        // Show the first slide
        slides[currentSlide].style.display = 'block';

        slider.querySelector('.right-arrow').addEventListener('click', () => {
            slides[currentSlide].style.display = 'none';
            currentSlide = (currentSlide + 1) % slides.length;
            slides[currentSlide].style.display = 'block';
        });

        slider.querySelector('.left-arrow').addEventListener('click', () => {
            slides[currentSlide].style.display = 'none';
            currentSlide = (currentSlide - 1 + slides.length) % slides.length;
            slides[currentSlide].style.display = 'block';
        });
    });
});
